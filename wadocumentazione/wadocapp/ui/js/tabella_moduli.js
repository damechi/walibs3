// classe wapagina
var wapagina = new Class
(
	{
	//-------------------------------------------------------------------------
	// extends
	Extends: wadocapp,

	//-------------------------------------------------------------------------
	// proprieta'

	//-------------------------------------------------------------------------
	//initialization
			
	//-------------------------------------------------------------------------
	azione_watabella_Controlli: function (id)
		{
			
		var nome_pagina_ritorno = escape
							(
							"Modulo: " + 
								this.tabella.righe[id].campi.nomePagina + "/" + 
								(
								 this.tabella.righe[id].campi.titolo ?
								 this.tabella.righe[id].campi.titolo :
								 this.tabella.righe[id].campi.nome
								)
							);
		this.apriPagina("tabella_controlli.php?idModulo=" + id + 
						"&nomepaginaritorno=" + nome_pagina_ritorno);
		}
		
	}
);

//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
//-----------------------------------------------------------------------------
document.wapagina = new wapagina();




